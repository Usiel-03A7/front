class Product {
  constructor(id, name, price, description, image, imageName, specs, monthsWithoutInterest) {
    this.id = id;
    this.name = name;
    this.price = price;
    this.description = description;
    this.image = image;
    this.imageName = imageName;
    this.specs = specs;
    this.monthsWithoutInterest = monthsWithoutInterest;
  }
}

export default Product;
